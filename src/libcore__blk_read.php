function libcore__blk_read($handle, $str_size)
{
	$str = '';
	for (;;)
	{
		$read_size = $str_size - strlen($str);
		if ($read_size === 0) break;

		$rc = fread($handle, $read_size);
		if ($rc === false) return false;

		$str = $str.$rc;
	}

	return $str;
}
