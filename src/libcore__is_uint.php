/**
 * check uint
 * \param[in] val source value
 * \return uint flag
 */
function libcore__is_uint($x)
{
	settype($x, "string");

	$size = strlen($x);

	if ($size === 0) return false;

	for ($i=0; $i < $size; $i++)
	{
		$ch = $x[$i];

		if
		(
			(ord($ch) >= ord('0')) && (ord($ch) <= ord('9'))
		)
		{
			continue;
		}

		return false;
	}

	return true;
}
