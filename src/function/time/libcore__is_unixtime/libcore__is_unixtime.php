/**
 * is unixtime?
 * \param[in] unixtime time in seconds like 0 or 1528799349 etc.
 * \return result true if unixtime or false if not
 */
function libcore__is_unixtime($unixtime)
{
	settype($unixtime, "string");


	if (strlen($unixtime) === 0)
	{
		return false;
	}

	if (libcore__is_uint($unixtime) === false)
	{
		return false;
	}


	return true;
}
