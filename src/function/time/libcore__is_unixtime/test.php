/**
 * test libcore__is_unixtime()
 */
(function()
{
	$__FUNCTION__='libcore__is_unixtime';
	$start = libcore__get_unixmicrotime();


	$rc = libcore__is_unixtime('');
	if ($rc !== false)
	{
		echo "ERROR[".$__FUNCTION__."()]: step001\n";
		exit(1);
	}


	$rc = libcore__is_unixtime('0z');
	if ($rc !== false)
	{
		echo "ERROR[".$__FUNCTION__."()]: step002\n";
		exit(1);
	}


	$rc = libcore__is_unixtime('1625819529');
	if ($rc !== true)
	{
		echo "ERROR[".$__FUNCTION__."()]: step003\n";
		exit(1);
	}


	$stop = libcore__get_unixmicrotime();
	echo $__FUNCTION__."(): ".($stop - $start)."\n"; flush();
})();
