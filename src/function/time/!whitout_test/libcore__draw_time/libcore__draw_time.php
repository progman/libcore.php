/**
 * draw time for log lines
 * \param[in] desc description
 * \return drow time
 */
function libcore__draw_time($desc = '')
{
	$tmp = "[".date("Y-m-d H:i:s", time())."]";

	if ($desc === '')
	{
		return $tmp.": ";
	}

	return $tmp." [".$desc."]: ";
}
