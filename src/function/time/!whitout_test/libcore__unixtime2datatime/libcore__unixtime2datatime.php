/**
 * convert from '1394767999' to '2014-03-14 03:33:19'
 * \param[in] unixtime unixtime
 * \return datatime
 */
function libcore__unixtime2datatime($unixtime)
{
	return date('Y-m-d H:i:s', $unixtime);
}
