	if (strcmp($rc, "1528761600000000") !== 0)
	{
		echo "ERROR[".$__FUNCTION__."()]: step001\n";
		exit(1);
	}


	$rc = libcore__unixmicrotime_truncate_to_day('');
	if ($rc !== false)
	{
		echo "ERROR[".$__FUNCTION__."()]: step002\n";
		exit(1);
	}


