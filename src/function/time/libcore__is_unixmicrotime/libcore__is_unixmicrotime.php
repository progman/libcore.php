/**
 * is unixmicrotime?
 * \param[in] unixmicrotimetime time in microseconds like 0000000 or 1528799349866429 etc.
 * \return result true if unixmicrotime or false if not
 */
function libcore__is_unixmicrotime($unixmicrotime)
{
	settype($unixmicrotime, "string");


	if (strlen($unixmicrotime) < 7)
	{
		return false;
	}

	if (libcore__is_uint($unixmicrotime) === false)
	{
		return false;
	}


	return true;
}
