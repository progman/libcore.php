/**
 * Base 64 Encoding with URL (https://tools.ietf.org/html/rfc4648#section-5)
 * \param[in] data source value
 * \return encoded data
 */
function libcore__base64url_encode(string $data)
{
	$tmp = base64_encode($data);


	$tmp = str_replace(['+', '/', '='], ['-', '_', ''], $tmp);


	return $tmp;
}
