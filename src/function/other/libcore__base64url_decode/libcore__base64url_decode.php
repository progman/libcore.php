/**
 * Base 64 Decoding with URL (https://tools.ietf.org/html/rfc4648#section-5)
 * \param[in] data source value
 * \return decoded data
 */
function libcore__base64url_decode($data)
{
	$tmp = str_replace(['-', '_'], ['+', '/'], $data);


	switch (strlen($tmp))
	{
		case 2:
		{
			$tmp .= "==";
			break;
		}
		case 3:
		{
			$tmp .= "=";
			break;
		}
		default:
		{
			break;
		}
	}


	return base64_decode($tmp);
}
