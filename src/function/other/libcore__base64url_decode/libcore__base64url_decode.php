/**
 * Base 64 Decoding with URL (https://tools.ietf.org/html/rfc4648#section-5)
 * \param[in] data source value
 * \param[in] strict use only base64 alphabet
 * \return decoded data
 */
function libcore__base64url_decode(string $data, bool $strict = true)
{
//	$tmp = str_replace(['-', '_'], ['+', '/'], $data);
	$tmp = strtr($data, '-_', '+/');


	switch (strlen($tmp))
	{
		case 2:
		{
			$tmp .= "==";
			break;
		}
		case 3:
		{
			$tmp .= "=";
			break;
		}
		default:
		{
			break;
		}
	}


	return base64_decode($tmp, $strict);
}
