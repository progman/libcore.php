/**
 * convert from flag to boolean
 * \param[in] val value
 * \param[in] value_default default value
 * \return result
 */
function libcore__flag2bool($val, $value_default = false)
{
	if (libcore__is_flag($val) === false)
	{
		return $value_default;
	}

	if (libcore__is_flag_set($val) === false)
	{
		return false;
	}

	return true;
}
