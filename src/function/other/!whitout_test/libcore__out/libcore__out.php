/**
 * make output json
 * \param[in] list output object list
 * \param[in] callback JSONP callback
 * \return output json
 */
function libcore__out($list = array(), $callback = '')
{
	if ($callback != '')
	{
		if ($callback != '?')
		{
			echo $callback."(\n";
		}
	}

	$obj = (object)$list;

	echo json_encode($obj, JSON_PRETTY_PRINT);

	if ($callback != '')
	{
		if ($callback != '?')
		{
			echo ");";
		}
	}

	echo "\n";
}
