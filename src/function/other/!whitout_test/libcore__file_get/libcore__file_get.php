/**
 * get file body
 * \param[in] filename name of file
 * \return file body
 */
function libcore__file_get($filename)
{
	$rc = @fopen($filename, 'rb');
	if ($rc === false) return false;
	$handle = $rc;


	$rc = @stat($filename);
	if ($rc === false) return false;
	$stat = $rc;
	$size = $stat['size'];


	$rc = libcore__blk_read($handle, $size);
	if ($rc === false) return false;
	$str = $rc;


	$rc = @fclose($handle);
	if ($rc === false) return false;


	return $str;
}
