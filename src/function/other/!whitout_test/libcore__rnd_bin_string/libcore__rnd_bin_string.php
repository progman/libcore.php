/**
 * get rnd bin string
 * \param[in] size count of bytes
 * \return rnd bin string
 */
function libcore__rnd_bin_string($size)
{
	$crypto_strong = true;
	$raw = openssl_random_pseudo_bytes($size, $crypto_strong);
	if ($raw === false) return false;

	return $raw;
}
