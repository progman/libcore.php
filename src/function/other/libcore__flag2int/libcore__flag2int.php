/**
 * convert from flag to int
 * \param[in] val value
 * \param[in] value_default default value
 * \return result
 */
function libcore__flag2int($val, $value_default = 0)
{
	if (libcore__is_flag($val) === false)
	{
		return $value_default;
	}

	if (libcore__is_flag_set($val) === false)
	{
		return 0;
	}

	return 1;
}
