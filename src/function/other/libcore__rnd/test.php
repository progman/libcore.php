/**
 * test libcore__rnd()
 */
(function()
{
	$max = 100;
	for ($i=0; $i < ($max + 1); $i++)
	{
		$count = 0;
		for (;;)
		{
			$x = libcore__rnd(0, $max);
			if ($x == $i) break;

			if ($x == ($max + 1))
			{
				echo "ERROR[".__FUNCTION__."()]: step001\n";
				exit(1);
			}

			$count++;

			if ($count == 1000000000)
			{
				echo "ERROR[".__FUNCTION__."()]: step002\n";
				exit(1);
			}
		}
	}
})();
