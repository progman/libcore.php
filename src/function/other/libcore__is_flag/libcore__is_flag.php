/**
 * check is flag
 * \param[in] val source value (true::bool, not 0::int, "true"::string, "on"::string, "1"::string)
 * \return is flag
 */
function libcore__is_flag($val)
{
	if
	(
		(libcore__is_flag_unset($val) !== false) ||
		(libcore__is_flag_set($val)   !== false)
	)
	{
		return true;
	}

	return false;
}
