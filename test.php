function test__libcore__hex2bin()
{
	$x = 'super puper';

	$x_hex = bin2hex($x);

	$y = libcore__hex2bin($x_hex, true);

	if ($x !== $y)
	{
		echo "ERROR[test__libcore__hex2bin()]: step001\n";
		exit(1);
	}
}
